# Creating a list
my_list = ["apple", "banana", "cherry", "orange"]

# Accesing elements in the list
print(my_list[0])
print(my_list[-1])

# Adding and removing elements
my_list.append("grape")
print(my_list)

my_list.remove("banana")
print(my_list)

# Slicing a list
sublist = my_list[1:3]
print(sublist)
